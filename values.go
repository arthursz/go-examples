package main

import "fmt"

func main() {
    fmt.Println("go" + "lang") // golang

    fmt.Println("1 + 1 =", 1 + 1) // 1 + 1 = 2
    fmt.Println("2.0/3.0 =", 2.0/3.0) // 2.0/3.0 = 0.6666666666666666

    fmt.Println(true && false) // false
    fmt.Println(true || false) // true
    fmt.Println(!true) // false
}
